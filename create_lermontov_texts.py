ТЕКСТ_1 = """
Герой нашего времени (отрывок)

Бэла

Я ехал на перекладных из Тифлиса. Вся дорога шла через горы. Погода была ужасная.
— Что это за страна! — воскликнул я с досадой.
Мой ямщик, осетин, только улыбнулся в ответ.

Встретили мы офицера, который возвращался из Персии. Он был бледен и задумчив.
— Позвольте присоединиться к вам, — сказал он тихо. — Мне скучно одному.
Мы разговорились. Он рассказал мне свою историю.

Это было давно, еще на Кавказе. Я служил тогда в крепости.
— Знаете ли, что такое скука? — спросил меня однажды мой собеседник.
Я молчал. Он продолжал:
— Скука — это когда не знаешь, чего хочешь.

Однажды привезли в крепость девушку. Звали ее Бэла.
— Она была прекрасна, как горный цветок, — говорил он с волнением.
Ее глаза сияли, как звезды в южную ночь.

Но что я могу рассказать вам о любви? Вы не поймете меня.
— Любовь для нас, военных, — продолжал он, — это лишь эпизод.
И он замолчал, устремив взгляд вдаль.
"""

ТЕКСТ_2 = """
Мцыри (отрывок)

Немногие минуты жизни
Ты б мне дал за эту жизнь?

Ты скажешь: зачем я пишу так много? Зачем эти строки?
— Потому что душа моя полна! — отвечал бы я тебе.
Потому что сердце мое жаждет свободы!

Я рос в неволе, среди чужих людей. Монастырь стал моим домом.
— Но разве это дом? — спрашивал я себя снова и снова.
Нет, это темница! Темница для моей мятежной души!

Однажды ночью я бежал. Бежал без оглядки, как раненый зверь.
Горы встречали меня грозным величием. Лес шумел над головой.
— Свобода! — кричал я в восторге. — Наконец-то ты моя!

Три дня я скитался по лесам. Три дня я был счастлив!
— И что же? — спросишь ты. — Неужели это все?
Да, все. Но эти три дня стоили целой жизни!

Я встретил барса в горах. Мы боролись с ним насмерть.
— Кто победил? — удивишься ты.
Победила природа. Победила дикая, свободная стихия!
"""

